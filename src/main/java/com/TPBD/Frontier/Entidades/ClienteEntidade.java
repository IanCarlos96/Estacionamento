package com.TPBD.Frontier.Entidades;

/**
 *
 * @author IanCarlosAfonsodaSil
 */
public class ClienteEntidade {
    private int id;
    private String nome;
    private String cpf;
    private String email;
    private String telefone;
    private String cidade;
    private String endereco;
    private String cep;
    private int idEmpresa;

    public ClienteEntidade(int id, String nome, String cpf, String email, String telefone, String cidade, String endereco, String cep, int idEmpresa) {
        this.id = id;
        this.nome = nome;
        this.cpf = cpf;
        this.email = email;
        this.telefone = telefone;
        this.cidade = cidade;
        this.endereco = endereco;
        this.cep = cep;
        this.idEmpresa = idEmpresa;
    }

    public ClienteEntidade() {
    }

    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public int getIdEmpresa() {
        return idEmpresa;
    }

    public void setIdEmpresa(int idEmpresa) {
        this.idEmpresa = idEmpresa;
    }
    
    
    
}
