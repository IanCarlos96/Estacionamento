    private DefaultListModel geraVeiculosEstacionadosListModel(){
        ArrayList<VeiculoEntidade> a = this.geraVeiculosEstacionadosList();
        DefaultListModel listModel = new DefaultListModel();
        for(int i=0; i< a.size(); i++){
            listModel.addElement(a.get(i).getPlaca());
        }
        return listModel;
    }
    
    private ArrayList <VeiculoEntidade> geraVeiculosEstacionadosList(){
        this.fluxosAbertosList = this.fluxoVeiculoBusiness.listaFluxosAbertos();
        this.veiculosEstacionadosList = new ArrayList();
        for(int i=0; i < this.fluxosAbertosList.size(); i++) {
            this.veiculosEstacionadosList.add(this.fluxoVeiculoBusiness.veiculoBusiness.buscaVeiculo(this.fluxosAbertosList.get(i).getIdVeiculo()));
        }
        return veiculosEstacionadosList;
    }
