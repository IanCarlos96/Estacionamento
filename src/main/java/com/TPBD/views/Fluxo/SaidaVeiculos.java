    private void JListVeiculosEstacionadosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JListVeiculosEstacionadosMousePressed
        // TODO add your handling code here:
        VeiculoEntidade veiculo = veiculosEstacionadosList.get(this.JListVeiculosEstacionados.getSelectedIndex());
        FluxoVeiculoEntidade fluxoAtual = fluxosAbertosList.get(this.JListVeiculosEstacionados.getSelectedIndex());
        
        this.JFVeiculoInfo.setText("Informações do Veículo\n"
                +"\nMarca: "+veiculo.getMarca()
                +"\nModelo: "+veiculo.getModelo()
                +"\nPlaca: "+veiculo.getPlaca()
        );
        if(fluxoAtual.getHorarioEntrada()!= null)
            this.JLHorarioText.setText(fluxoAtual.getHorarioEntrada().format(DateTimeFormatter.ISO_LOCAL_TIME));
        else {
            this.JLHorarioText.setText("");
        }
    }//GEN-LAST:event_JListVeiculosEstacionadosMousePressed

