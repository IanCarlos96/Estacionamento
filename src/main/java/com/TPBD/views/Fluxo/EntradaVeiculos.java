        // TODO add your handling code here:
        int vagaIndex;
        
        int idVagaFromCombox = this.JComboVagas.getSelectedIndex();
        if(this.vagaManual){
            vagaIndex = this.obtemIndexGenerico(this.vagaLivreList, "vaga", this.JComboVagas.getSelectedIndex());
        } else {
            vagaIndex = this.vagaLivreList.get(0).getId();
        }
        int idVeiculoFromCombox = this.JComboVeiculos.getSelectedIndex();
        
        this.fluxo.setIdVaga(vagaLivreList.get(vagaIndex).getId());
        this.fluxo.setIdVeiculo(veiculoList.get(this.obtemIndexGenerico(veiculoList, "veiculo", idVeiculoFromCombox)).getId());
        this.fluxo.setGerarRecibo("0");
        
        int status = this.fluxoBusiness.cadastraFluxo(fluxo);
        if(status == 0) {
            //Limpa a tela, refaz as listas, blablabla
        }
