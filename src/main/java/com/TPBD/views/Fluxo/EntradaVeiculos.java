    private String[] geraListaModelComboBox(ArrayList obList, String nomeCampo) {
        ArrayList<String> aux = new ArrayList();
        if(!obList.isEmpty()) {
            for(int i=0; i < obList.size(); i++){
                if(nomeCampo.equals("veiculo")) {
                    VeiculoEntidade veiculo = (VeiculoEntidade) obList.get(i);
                    aux.add(veiculo.getPlaca());
                }
                if(nomeCampo.equals("vaga")) {
                    VagaEntidade vaga = (VagaEntidade) obList.get(i);
                    aux.add(vaga.getNomeVaga());
                }
            }
        } 
        String[] teste = new String[aux.size()];
        teste = aux.toArray(teste);
        
        return teste;
    }
    
    private int obtemIndexGenerico(ArrayList obList, String nomeCampo, int idFromCombox) {
        //Implementar o obtem Index para vagas e para veículos
        for(int i =0; i < obList.size(); i++) {
            if(nomeCampo.equals("veiculo")){
                if(idFromCombox == i){
                    return i;
                }
            }
            if(nomeCampo.equals("vaga")){
                if(idFromCombox == i){
                    return i;
                }
            }
        }
        return -1;
    }
    
    
