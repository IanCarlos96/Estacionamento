    private void editarEmpresaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarEmpresaButtonActionPerformed
        // TODO add your handling code here:
        boolean finded = false;
        if(JEmpresasList.isSelectionEmpty()) {
            this.insertEmpresaButtonActionPerformed(evt);
        }
        EmpresaEntidade empresa = new EmpresaEntidade();
        for(int i = 0; i< empresaList.size(); i++){
            if(this.JEmpresasList.getSelectedValue().toString().equals(empresaList.get(i).getRazaoSocial())){
                empresa = empresaList.get(i);
                finded = true;
            }
        }
        if(finded){
            Edicao edicaoView = new Edicao(empresa);
            edicaoView.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            edicaoView.setVisible(true);
        }
    }//GEN-LAST:event_editarEmpresaButtonActionPerformed

    private void excluirEmpresaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirEmpresaButtonActionPerformed
        // TODO add your handling code here:
        EmpresaEntidade empresa = new EmpresaEntidade();
        empresa.setId(-1);
        for(int i = 0; i< empresaList.size(); i++){
            if(this.JEmpresasList.getSelectedValue().toString().equals(empresaList.get(i).getRazaoSocial())){
                empresa = empresaList.get(i);
            }
        }
        if(empresa.getId() != -1) {
            int status = empresaBusiness.excluiEmpresa(empresa.getId());
            if(status == 0) {
                this.empresaList = empresaBusiness.listaEmpresas();
                this.JEmpresasList.setModel(this.geraListaModel());
            }
        }

    }//GEN-LAST:event_excluirEmpresaButtonActionPerformed

