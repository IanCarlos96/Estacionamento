    private void editarVagaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarVagaButtonActionPerformed
        // TODO add your handling code here:
        VagaEntidade vaga = new VagaEntidade();
        for(int i = 0; i< vagaList.size(); i++){
            if(this.JVagasList.getSelectedValue().toString().equals(vagaList.get(i).getNomeVaga())){
                vaga = vagaList.get(i);
            }
        }
        Edicao edicaoView = new Edicao(vaga);
        edicaoView.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        edicaoView.setVisible(true);
    }//GEN-LAST:event_editarVagaButtonActionPerformed

    private void excluirVagaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirVagaButtonActionPerformed
        // TODO add your handling code here:
        VagaEntidade vaga = new VagaEntidade();
        vaga.setId(-1);
        for(int i = 0; i< vagaList.size(); i++){
            if(this.JVagasList.getSelectedValue().toString().equals(vagaList.get(i).getNomeVaga())){
                vaga = vagaList.get(i);
            }
        }
        if(vaga.getId() != -1) {
            int status = vagaBusiness.deleteVaga(vaga.getId());
            if(status == 0) {
                this.vagaList = vagaBusiness.buscaListaVagas();
                this.JVagasList.setModel(this.geraListaModel());
            }
        }
        
    }//GEN-LAST:event_excluirVagaButtonActionPerformed

    private DefaultListModel geraListaModel() {
        defaultListModel = new DefaultListModel();
        if(!vagaList.isEmpty()) {
            for(int i=0; i < vagaList.size(); i++){
                defaultListModel.addElement(vagaList.get(i).getNomeVaga());
            }
        } else {
            defaultListModel.addElement("Lista Vazia!");
        }
        return defaultListModel;
    }
    
