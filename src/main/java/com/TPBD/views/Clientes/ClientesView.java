    private void editarClienteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarClienteButtonActionPerformed
        // TODO add your handling code here:
        ClienteEntidade cliente = new ClienteEntidade();
        for(int i = 0; i< clienteList.size(); i++){
            if(this.JClienteList.getSelectedValue().toString().equals(clienteList.get(i).getNome())){
                cliente = clienteList.get(i);
            }
        }
        ArrayList<EmpresaEntidade> empresaList = clienteBusiness.empresaBusiness.listaEmpresas();
        Edicao edicaoView = new Edicao(cliente, empresaList);
        edicaoView.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        edicaoView.setVisible(true);

    }//GEN-LAST:event_editarClienteButtonActionPerformed

    private void excluirClienteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirClienteButtonActionPerformed
        // TODO add your handling code here:
        ClienteEntidade cliente = new ClienteEntidade();
        cliente.setId(-1);
        for(int i = 0; i< clienteList.size(); i++){
            if(this.JClienteList.getSelectedValue().toString().equals(clienteList.get(i).getNome())){
                cliente = clienteList.get(i);
            }
        }
        if(cliente.getId() != -1) {
            int status = clienteBusiness.excluiCliente(cliente.getId());
            if(status == 0) {
                this.clienteList = clienteBusiness.buscaListaCliente();
                this.JClienteList.setModel(this.geraListaModel());
                
            }
        }
    }//GEN-LAST:event_excluirClienteButtonActionPerformed

