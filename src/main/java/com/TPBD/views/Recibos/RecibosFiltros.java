    private String[] geraListaModel(ArrayList list, String tipo){
        ArrayList<String> aux = new ArrayList();
        
        if(tipo.equals("recibos")){
            ArrayList<ReciboEntidade> lista = list;
            ArrayList<VeiculoEntidade> veiculoList = veiculoBusiness.listaVeiculos();
            if(!lista.isEmpty()) {
                for(int i=0; i < lista.size(); i++){
                    for(int j=0; j< veiculoList.size(); j++) {
                        if(veiculoList.get(j).getId() == lista.get(i).getIdVeiculo()){
                            aux.add(veiculoList.get(j).getPlaca());
                        }
                    }
                }
            }
        }
        if(tipo.equals("empresa")){
            ArrayList<EmpresaEntidade> lista = list;
            if(!lista.isEmpty()){
                for(int i=0; i < lista.size(); i++){
                    aux.add(lista.get(i).getRazaoSocial());
                }
            }
        }
        if(tipo.equals("cliente")){
            ArrayList<ClienteEntidade> lista = list;
            if(!lista.isEmpty()){
                for(int i=0; i < lista.size(); i++){
                    aux.add(lista.get(i).getNome());
                }
            }
        }
        String[] teste = new String[aux.size()];
        teste = aux.toArray(teste);
        
        return teste;
    }
    
    public String preparaDadosRecibo(String placa) {
        ArrayList<VeiculoEntidade> veiculoList = this.veiculoBusiness.listaVeiculos();
        VeiculoEntidade veiculo = new VeiculoEntidade();
        for(int i=0; i < veiculoList.size(); i++){
            if(veiculoList.get(i).getPlaca().equals(placa))
                veiculo = veiculoList.get(i);
        }
        ReciboEntidade recibo = new ReciboEntidade();
        for(int j=0; j< this.listaRecibos.size(); j++){
            if(this.listaRecibos.get(j).getIdVeiculo() == veiculo.getId()){
                recibo = this.listaRecibos.get(j);
            }
        }
        return "Dados do recibo"
                + "\nVeículo: "+ veiculo.getMarca()+" "+veiculo.getModelo()+"\n"+veiculo.getPlaca()+
                "\nEntrada: "+recibo.getHorarioEntrada().format(DateTimeFormatter.ISO_TIME) +
                "\nSaída: "+recibo.getHorarioSaida().format(DateTimeFormatter.ISO_TIME) +
                "\nValor: "+recibo.getValor()
                ;
        
    }
    
