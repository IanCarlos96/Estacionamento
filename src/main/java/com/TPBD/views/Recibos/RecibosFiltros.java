    private void JBFClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBFClienteActionPerformed
        // TODO add your handling code here:
        String cliente = this.JComboCliente.getSelectedItem().toString();
        int idCliente = -1;
        ArrayList<ClienteEntidade> clienteList = this.clienteBusiness.buscaListaCliente();
        for(int i=0; i< clienteList.size(); i++) {
            if(clienteList.get(i).getNome().equals(cliente)){
                idCliente = clienteList.get(i).getId();
            }
        }
        listaRecibos = this.reciboBusiness.buscaRecibosPorClienteAberto(idCliente);
        this.JComboRecibos.setModel(new DefaultComboBoxModel(this.geraListaModel(listaRecibos, "recibos")));

    }//GEN-LAST:event_JBFClienteActionPerformed

